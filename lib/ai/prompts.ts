function getMacroTargets(
  goal: string,
  calories: number
): { protein_g: number; carb_g: number; fat_g: number } {
  const profiles: Record<string, { protein: number; carb: number; fat: number }> = {
    "ganho de massa": { protein: 0.30, carb: 0.45, fat: 0.25 },
    "perda de peso":  { protein: 0.35, carb: 0.35, fat: 0.30 },
    "manutenção":     { protein: 0.25, carb: 0.50, fat: 0.25 },
  };

  const profile = profiles[goal] ?? profiles["manutenção"];

  return {
    protein_g: Math.round((calories * profile.protein) / 4),
    carb_g:    Math.round((calories * profile.carb) / 4),
    fat_g:     Math.round((calories * profile.fat) / 9),
  };
}

function getCalorieDistribution(
  calories: number,
  meals: Array<"breakfast" | "lunch" | "snack" | "dinner">
): Record<string, number> {
  const weights: Record<string, number> = {
    breakfast: 0.20,
    lunch:     0.35,
    snack:     0.10,
    dinner:    0.35,
  };

  const activeMeals = meals.filter((m) => weights[m] !== undefined);
  const totalWeight = activeMeals.reduce((sum, m) => sum + weights[m], 0);

  return Object.fromEntries(
    activeMeals.map((m) => [m, Math.round((calories * weights[m]) / totalWeight)])
  );
}

